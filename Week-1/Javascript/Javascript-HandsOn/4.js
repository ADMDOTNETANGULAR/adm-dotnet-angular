//javascript operators
// Arithmetic operators
let a = 5;
let b = 10;
let sum = a + b; // addition
let difference = b - a; // subtraction
let product = a * b; // multiplication
let quotient = b / a; // division
let remainder = b % a; // modulus
// Increment and Decrement operators
let count = 0;
count++; // increment
count--; // decrement
// Assignment operators

let c = 15;
c += 5; // c = c + 5
c -= 3; // c = c - 3
c *= 2; // c = c * 2
c /= 4; // c = c / 4
c %= 3; // c = c % 3
// Comparison operators
let isEqual = (a === b); // strict equality
let isNotEqual = (a !== b); // strict inequality
let isGreater = (a > b); // greater than
let isLess = (a < b); // less than
let isGreaterOrEqual = (a >= b); // greater than or equal
let isLessOrEqual = (a <= b); // less than or equal
// Logical operators
let isTrue = true;
let isFalse = false;
let andCondition = isTrue && isFalse; // logical AND
let orCondition = isTrue || isFalse; // logical OR
let notCondition = !isTrue; // logical NOT
// Ternary operator
let age = 18;
let canVote = (age >= 18) ? "Yes" : "No"; // ternary operator

